function checkGenerateCondition() {
    const txtArea = document.getElementById('postTextArea');
    const genBtn = document.getElementById('btnGeneratePost');
    if(txtArea.value.trim().length > 0 || selectedNetworks.size > 0) genBtn.style.display = 'flex';
    else genBtn.style.display = 'none';
}

function renderPosts(filter) {
    const container = document.getElementById('simulatedPostsList');
    container.innerHTML = '';
    const toShow = filter === 'all' ? simulatedPosts : simulatedPosts.filter(p => p.net === filter);

    if (toShow.length === 0) {
        container.innerHTML = `<p style="color: var(--text-secondary); grid-column: 1 / -1; text-align: center;">No hay publicaciones asociadas a esta red.</p>`;
        return;
    }

    toShow.forEach(post => {
        let iconHtml = '', iconClass = '';
        if(post.net === 'ig') { iconHtml = '<i class="fab fa-instagram"></i>'; iconClass = 'si-instagram'; }
        if(post.net === 'fb') { iconHtml = '<i class="fab fa-facebook-f"></i>'; iconClass = 'si-facebook'; }
        if(post.net === 'tw') { iconHtml = '<i class="fa-brands fa-x-twitter"></i>'; iconClass = 'si-twitter'; }
        if(post.net === 'tk') { iconHtml = '<i class="fab fa-tiktok"></i>'; iconClass = 'si-tiktok'; }

        const card = document.createElement('div');
        card.className = 'post-card';
        card.innerHTML = `
            <div class="post-card-header"><div class="social-icon ${iconClass}">${iconHtml}</div><span style="font-weight: 600;">@tu_marca</span></div>
            <img src="${post.img}" class="post-card-img" alt="Post Img">
            <div class="post-card-content">${post.text}</div>
            <div class="post-card-stats"><span><i class="fas fa-heart"></i> ${Math.floor(Math.random()*50)+1}k</span><span><i class="fas fa-comment"></i> ${Math.floor(Math.random()*500)+10}</span></div>
        `;
        container.appendChild(card);
    });
}

function showToast(message) {
    const toast = document.createElement('div');
    toast.className = 'toast';
    toast.innerText = message;
    document.body.appendChild(toast);
    setTimeout(() => toast.classList.add('show'), 100);
    setTimeout(() => { toast.classList.remove('show'); setTimeout(() => toast.remove(), 300); }, 3000);
}
