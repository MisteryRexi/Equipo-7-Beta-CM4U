function setLanguage(lang) {
    currentLang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang][key]) {
            if(el.tagName === 'INPUT' && el.type !== 'checkbox' && el.type !== 'radio') el.placeholder = i18n[lang][key];
            else el.innerHTML = i18n[lang][key];
        }
    });

    document.getElementById('btnLangEs').className = lang === 'es' ? 'btn btn-primary' : 'btn btn-secondary';
    document.getElementById('btnLangEn').className = lang === 'en' ? 'btn btn-primary' : 'btn btn-secondary';
    document.getElementById('langToggleNav').innerText = lang === 'es' ? 'EN' : 'ES';
}

