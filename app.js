function initTypewriter() {
    const el = document.getElementById('typewriter');
    if (!el) return;
    let loopNum = 0, isDeleting = false, txt = '', typeSpeed = 100;
    function tick() {
        const strDict = i18n[currentLang] || i18n['es'];
        const twTexts = [strDict.tw1, strDict.tw2, strDict.tw3, strDict.tw4];
        let i = loopNum % twTexts.length;
        let fullTxt = twTexts[i];
        if (isDeleting) { txt = fullTxt.substring(0, txt.length - 1); typeSpeed = 50; } 
        else { txt = fullTxt.substring(0, txt.length + 1); typeSpeed = 100; }
        el.innerHTML = txt;
        if (!isDeleting && txt === fullTxt) { typeSpeed = 2000; isDeleting = true; } 
        else if (isDeleting && txt === '') { isDeleting = false; loopNum++; typeSpeed = 500; }
        setTimeout(tick, typeSpeed);
    }
    tick();
}

function initScrollAnimations() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) { entry.target.classList.add('visible'); }
        });
    }, { threshold: 0.1 });
    document.querySelectorAll('.hidden-scroll').forEach(el => observer.observe(el));
}

function initAuth() {
    const authBtns = document.querySelectorAll('.auth-btn');
    const modal = document.getElementById('auth-modal');
    authBtns.forEach(btn => btn.addEventListener('click', () => modal.classList.add('active')));
    document.getElementById('closeAuthModal').addEventListener('click', () => modal.classList.remove('active'));

    document.getElementById('authForm').addEventListener('submit', (e) => {
        e.preventDefault();
        modal.classList.remove('active');
        document.getElementById('landing-page').classList.remove('active');
        document.getElementById('landing-page').classList.add('hidden');
        
        const dashboard = document.getElementById('dashboard-page');
        dashboard.classList.remove('hidden');
        setTimeout(() => dashboard.classList.add('active'), 50);

        const email = document.getElementById('authEmail').value;
        document.getElementById('userAvatar').innerText = email ? email.charAt(0).toUpperCase() : 'C';
    });

    document.getElementById('logoutBtn').addEventListener('click', () => {
        document.getElementById('dashboard-page').classList.remove('active');
        document.getElementById('dashboard-page').classList.add('hidden');
        const landing = document.getElementById('landing-page');
        landing.classList.remove('hidden');
        setTimeout(() => landing.classList.add('active'), 50);
    });
}

function initRouting() {
    const navItems = document.querySelectorAll('.nav-item');
    const sections = document.querySelectorAll('.dash-section');
    const sidebar = document.getElementById('dashboardSidebar');
    
    // Sidebar routing
    navItems.forEach(item => {
        item.addEventListener('click', () => {
            sidebar.classList.remove('open');
            navItems.forEach(nav => nav.classList.remove('active'));
            item.classList.add('active');
            
            const targetId = item.getAttribute('data-target');
            // If they click 'Inicio', reset create view to main view
            if(targetId === 'dash-main') {
                document.getElementById('dash-main').classList.add('active');
                document.getElementById('dash-create').classList.remove('active');
                // Ensure sibling sections hide properly
                sections.forEach(sec => { if(sec.id !== 'dash-main' && sec.id !== 'dash-create') sec.classList.remove('active'); });
            } else {
                sections.forEach(sec => {
                    if(sec.id === targetId) sec.classList.add('active');
                    else sec.classList.remove('active');
                });
            }
        });
    });

    // Big Button logic Dashboard
    document.getElementById('btnOpenCreateView').addEventListener('click', () => {
        document.getElementById('dash-main').classList.remove('active');
        document.getElementById('dash-create').classList.add('active');
        resetCreateView(); // Clear previous inputs visually
    });
    // Back to main Dashboard
    document.getElementById('btnBackToMain').addEventListener('click', () => {
        document.getElementById('dash-create').classList.remove('active');
        document.getElementById('dash-main').classList.add('active');
    });

    // Mobile sidebar toggle
    document.getElementById('openSidebar').addEventListener('click', () => sidebar.classList.add('open'));
    document.getElementById('closeSidebar').addEventListener('click', () => sidebar.classList.remove('open'));

    // User Profile & Notification Dropdown logic
    const userProfileBtn = document.getElementById('userProfileBtn');
    const userDropdown = document.getElementById('userDropdown');
    const notifProfileBtn = document.getElementById('notifProfileBtn');
    const notifDropdown = document.getElementById('notifDropdown');
    
    if(userProfileBtn && userDropdown) {
        userProfileBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            userDropdown.classList.toggle('active');
            if(notifDropdown) notifDropdown.classList.remove('active');
        });
    }

    if(notifProfileBtn && notifDropdown) {
        notifProfileBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            notifDropdown.classList.toggle('active');
            if(userDropdown) userDropdown.classList.remove('active');
        });
    }
        
    // Close dropdowns when clicking outside
    document.addEventListener('click', () => {
        if(userDropdown) userDropdown.classList.remove('active');
        if(notifDropdown) notifDropdown.classList.remove('active');
    });

    // Dropdown logout button routing
    const logoutBtnDropdown = document.getElementById('logoutBtnDropdown');
    if(logoutBtnDropdown) {
        logoutBtnDropdown.addEventListener('click', () => {
            document.getElementById('logoutBtn').click(); // trigger standard logout
        });
    }
}

